export interface Suburb {
  name: string
  postcode: string
  display: string
}

export const QLD_SUBURBS: Suburb[] = RAW.map(([name, postcode]) => ({
  name,
  postcode,
  display: `${name} ${postcode}`,
}))

export function searchSuburbs(query: string, limit = 8): Suburb[] {
  if (query.length < 2) return []
  const q = query.toLowerCase()
  const starts = QLD_SUBURBS.filter((s) => s.name.toLowerCase().startsWith(q))
  const contains = QLD_SUBURBS.filter(
    (s) => !s.name.toLowerCase().startsWith(q) && s.name.toLowerCase().includes(q)
  )
  return [...starts, ...contains].slice(0, limit)
}
